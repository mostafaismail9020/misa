package com.sap.ibso.eservices.storefront.forms;

public class SagiaVerificationForm {

	private String countryCode;
	private String mobile;
	private String regEmail;
	private String qeemahEmail;
	
	public String getCountryCode() {
		return countryCode;
	}
	public void setCountryCode(String countryCode) {
		this.countryCode = countryCode;
	}
	public String getMobile() {
		return mobile;
	}
	public void setMobile(String mobile) {
		this.mobile = mobile;
	}
	public String getRegEmail() {
		return regEmail;
	}
	public void setRegEmail(String regEmail) {
		this.regEmail = regEmail;
	}
	public String getQeemahEmail() {
		return qeemahEmail;
	}
	public void setQeemahEmail(String qeemahEmail) {
		this.qeemahEmail = qeemahEmail;
	}
	
	
}
